const GenerateStartByLvl = (lvl) => {
    const MaxStartLvl = 5
    var yellowStar = 0
    var greyStar = 0
    // In case, lvl is not define, or no level star, we will assume it as 5 stars
    if (lvl == undefined || lvl <= 0) {
        yellowStar = MaxStartLvl
        greyStar = 0
    } else {
        yellowStar = lvl
        greyStar = MaxStartLvl - lvl
    }
    var i
    var arrStar = []
    for (i = 1; i <= yellowStar; i++) {
        arrStar.push(
            <svg key={'yellow_'+i} className="mx-1 w-4 h-4 fill-current text-yellow-500" xmlns="http://www.w3.org/2000/svg" viewBox="0 0 20 20"><path d="M10 15l-5.878 3.09 1.123-6.545L.489 6.91l6.572-.955L10 0l2.939 5.955 6.572.955-4.756 4.635 1.123 6.545z" /></svg>
        )
    }
    for (i = 1; i <= greyStar; i++) {
        arrStar.push(<svg key={'grey_'+i} className="mx-1 w-4 h-4 fill-current text-gray-400" xmlns="http://www.w3.org/2000/svg" viewBox="0 0 20 20"><path d="M10 15l-5.878 3.09 1.123-6.545L.489 6.91l6.572-.955L10 0l2.939 5.955 6.572.955-4.756 4.635 1.123 6.545z" /></svg>)
    }
    return arrStar
}

export default GenerateStartByLvl